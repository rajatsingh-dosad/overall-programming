class Switch3 {

	public static void main(String[] args){

		char ch = 'A'; // datatype character
		
		switch(ch){           //int, short, byte, char, String
			case 'A':
				System.out.println("Case 1");
				break;

			case 'B':
				System.out.println("Case 2");
				break;


			default:
				System.out.println("No such case");//default case
				break;

		}

	}

}
