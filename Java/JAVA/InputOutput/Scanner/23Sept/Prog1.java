
import java.util.*;

class InputScanner {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);

		int x = sc.nextInt();
		String s = sc.next();
		float f = sc.nextFloat();
		char ch = sc.next().charAt(0);

		System.out.println(x);
		System.out.println(f);
		System.out.println(s);
		System.out.println(ch);
	}
}





